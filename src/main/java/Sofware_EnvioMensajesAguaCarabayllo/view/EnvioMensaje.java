    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 // Establecer la conexión a la base de datos
    ConexionSQL conexionSQL = new ConexionSQL();

    try (Connection connection = conexionSQL.getConnection()) {
        // Consulta SQL para obtener ID, Usuario y Numero
        String sql = "SELECT ID, Usuario, Numero FROM Mensajes";

        try (PreparedStatement statement = connection.prepareStatement(sql);
             ResultSet resultSet = statement.executeQuery()) {

            // Crear un modelo de tabla
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("Usuario");
            model.addColumn("Número");

            // Agregar los datos al modelo
            while (resultSet.next()) {
                int id = resultSet.getInt("ID");
                String usuario = resultSet.getString("Usuario");
                String numero = resultSet.getString("Numero");
                model.addRow(new Object[]{id, usuario, numero});
            }

            // Establecer el modelo en la tabla
            tbContactos.setModel(model);

        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al acceder a la base de datos: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        // Siempre cierra la conexión en un bloque finally
        conexionSQL.cerrarConexion();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

