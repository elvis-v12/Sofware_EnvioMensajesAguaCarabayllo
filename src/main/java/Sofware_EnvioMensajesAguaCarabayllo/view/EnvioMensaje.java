    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
// Configurar Twilio
Twilio.init(ACCOUNT_SID, AUTH_TOKEN);

// Obtener el modelo de la tabla
DefaultTableModel model = (DefaultTableModel) tbContactos.getModel();

// Encontrar el índice de la columna que contiene los números de teléfono
int columnaNumero = model.findColumn("Número");

// Verificar si se encontró la columna
if (columnaNumero != -1) {
    // Iterar sobre las filas de la tabla
    for (int i = 0; i < model.getRowCount(); i++) {
        // Convertir el valor a String
        Object valor = model.getValueAt(i, columnaNumero);
        String telefonoContacto = String.valueOf(valor);

        // Obtener la fecha de envío seleccionada
        java.util.Date fechaEnvioDate = jDateChooser1.getDate();
        java.sql.Date fechaEnvioSQL = new java.sql.Date(fechaEnvioDate.getTime());

        // Enviar mensaje utilizando Twilio
        try {
            Message message = Message.creator(
                    new com.twilio.type.PhoneNumber(telefonoContacto.trim()),
                    new com.twilio.type.PhoneNumber(numero),
                    txtMensaje.getText())
                .create();

            // Guardar el mensaje en la base de datos
            guardarMensajeEnBaseDeDatos(telefonoContacto, txtMensaje.getText(), fechaEnvioSQL);

            JOptionPane.showMessageDialog(null, "Mensaje enviado a " + telefonoContacto + ": " + message.getSid());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al enviar mensaje a " + telefonoContacto + ": " + e.getMessage());
        }
    }
} else {
    JOptionPane.showMessageDialog(null, "La columna 'Número' no fue encontrada en la tabla.");
}

    }//GEN-LAST:event_jButton7ActionPerformed

