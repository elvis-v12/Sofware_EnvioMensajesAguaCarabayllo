    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

       String placa=txtPlaca.getText();
       double capacidad=Double.parseDouble(txtCapacidad.getText());
       int grifos=Integer.parseInt(txtGrifos.getText());
        Cisterna Cisterna=new Cisterna(placa, capacidad, grifos);
        DefaultTableModel modeloTabla = (DefaultTableModel) jTable1.getModel();
Object[] fila = {Cisterna.getPlaca(), Cisterna.getCapacidad(),
                 Cisterna.getGrifos()};
modeloTabla.addRow(fila);
    
limpiarCamposFormulario();      
    }//GEN-LAST:event_jButton5ActionPerformed
